# crie um algoritmo que leia um numero e mostre seu 
# dobro seu tripo e raiz quadrada

numero = int(input("digite um numero: "))
dobro = numero * 2
triplo = numero * 3
raiz = numero ** (1/2)

print(f"O dobro de um {numero} é {dobro}")
print(f"O triplo de um {numero} é {triplo}")
print(f"A raiz quadrada de {numero} é igual {raiz}")
